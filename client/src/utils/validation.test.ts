import { describe, it, expect } from "vitest";
import {
  validateEmail,
  validatePhone,
  validateZipcode,
  validatePassword,
} from "./validation";

// ─────────────────────────────────────────────
// validateEmail
// ─────────────────────────────────────────────
describe("validateEmail", () => {
  // 説明：空文字の場合は必須エラーを返すこと
  // 条件：引数が空文字 ""
  it("空文字のとき必須エラーを返す", () => {
    expect(validateEmail("")).toBe("メールアドレスは必須です");
  });

  // 説明：@が含まれないなど不正な形式の場合はフォーマットエラーを返すこと
  // 条件：引数が "invalid-email"（@なし）
  it("不正な形式のときフォーマットエラーを返す", () => {
    expect(validateEmail("invalid-email")).toBe("正しい形式で入力してください");
  });

  // 説明：正しいメールアドレス形式のときは空文字を返すこと（エラーなし）
  // 条件：引数が "user@example.com"
  it("正しいメールアドレスのとき空文字を返す", () => {
    expect(validateEmail("user@example.com")).toBe("");
  });

  // 説明：ドメイン部分が1文字のTLDは不正とすること
  // 条件：引数が "user@example.c"（TLDが1文字）
  it("TLDが1文字のときフォーマットエラーを返す", () => {
    expect(validateEmail("user@example.c")).toBe("正しい形式で入力してください");
  });
});

// ─────────────────────────────────────────────
// validatePhone
// ─────────────────────────────────────────────
describe("validatePhone", () => {
  // 説明：空文字の場合は必須エラーを返すこと
  // 条件：引数が空文字 ""
  it("空文字のとき必須エラーを返す", () => {
    expect(validatePhone("")).toBe("電話番号は必須です");
  });

  // 説明：9桁以下や12桁以上はエラーを返すこと
  // 条件：引数が "123456789"（9桁）
  it("9桁の数字のときフォーマットエラーを返す", () => {
    expect(validatePhone("123456789")).toBe("10〜11桁の数字で入力してください");
  });

  // 説明：10桁の数字は正常とすること
  // 条件：引数が "0312345678"（10桁）
  it("10桁の数字のとき空文字を返す", () => {
    expect(validatePhone("0312345678")).toBe("");
  });

  // 説明：11桁の数字は正常とすること
  // 条件：引数が "09012345678"（11桁）
  it("11桁の数字のとき空文字を返す", () => {
    expect(validatePhone("09012345678")).toBe("");
  });
});

// ─────────────────────────────────────────────
// validateZipcode
// ─────────────────────────────────────────────
describe("validateZipcode", () => {
  // 説明：空文字の場合は必須エラーを返すこと
  // 条件：引数が空文字 ""
  it("空文字のとき必須エラーを返す", () => {
    expect(validateZipcode("")).toBe("郵便番号は必須です");
  });

  // 説明：6桁や8桁は不正とすること
  // 条件：引数が "123456"（6桁）
  it("6桁のときフォーマットエラーを返す", () => {
    expect(validateZipcode("123456")).toBe("7桁の数字で入力してください");
  });

  // 説明：7桁の数字は正常とすること
  // 条件：引数が "1234567"（7桁）
  it("7桁の数字のとき空文字を返す", () => {
    expect(validateZipcode("1234567")).toBe("");
  });
});

// ─────────────────────────────────────────────
// validatePassword
// ─────────────────────────────────────────────
describe("validatePassword", () => {
  // 説明：空文字の場合は必須エラーを返すこと
  // 条件：引数が空文字 ""
  it("空文字のとき必須エラーを返す", () => {
    expect(validatePassword("")).toBe("パスワードは必須です");
  });

  // 説明：7文字以下は短すぎるエラーを返すこと
  // 条件：引数が "abc123" （6文字）
  it("7文字以下のとき文字数エラーを返す", () => {
    expect(validatePassword("abc123")).toBe("8文字以上で入力してください");
  });

  // 説明：数字を含まない場合は英字+数字必須エラーを返すこと
  // 条件：引数が "abcdefgh"（英字のみ8文字）
  it("数字を含まないとき英字+数字必須エラーを返す", () => {
    expect(validatePassword("abcdefgh")).toBe("英字と数字を含めてください");
  });

  // 説明：英字を含まない場合は英字+数字必須エラーを返すこと
  // 条件：引数が "12345678"（数字のみ8文字）
  it("英字を含まないとき英字+数字必須エラーを返す", () => {
    expect(validatePassword("12345678")).toBe("英字と数字を含めてください");
  });

  // 説明：英字と数字を含む8文字以上は正常とすること
  // 条件：引数が "abc12345"（英字+数字8文字）
  it("英字と数字を含む8文字以上のとき空文字を返す", () => {
    expect(validatePassword("abc12345")).toBe("");
  });
});
